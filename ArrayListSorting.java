package Sorting;

import java.util.ArrayList;
import java.util.Collections;

public class ArrayListSorting {

	public static void main(String[] args) {

		ArrayList al = new ArrayList();
	     
	     al.add(100);
	     al.add(400);
	     al.add(300);
	     System.out.println(al);
	     Collections.sort(al);
	     System.out.println(al);
	     
	}

}
